// অ্যারিথমেটিক অপারেটর
let add = 4 + 4; // যোগ
let sub = 4 - 3; // বিয়োগ
let mul = 4 * 3; // গুণ
let div = 12 / 3; // ভাগ
let mod = 10 % 3; // মডুলাস (ভাগশেষ)
let exp = 2 ** 3; // এক্সপোনেনশিয়েশন (ঘাত)

// ইনক্রিমেন্ট এবং ডিক্রিমেন্ট
let inc = 5;
inc++; // ইনক্রিমেন্ট
let dec = 5;
dec--; // ডিক্রিমেন্ট

// অ্যাসাইনমেন্ট অপারেটর
let x = 5; // সাধারণ অ্যাসাইনমেন্ট
x += 3; // যোগ করে অ্যাসাইন
x -= 2; // বিয়োগ করে অ্যাসাইন
x *= 4; // গুণ করে অ্যাসাইন
x /= 2; // ভাগ করে অ্যাসাইন
x %= 3; // মডুলাস করে অ্যাসাইন

// তুলনামূলক অপারেটর
let eq = 5 == "5"; // সমান (টাইপ কনভার্শন সহ)
let seq = 5 === 5; // কঠোর সমান (টাইপ চেক সহ)
let neq = 5 != "4"; // সমান নয় (টাইপ কনভার্শন সহ)
let sneq = 5 !== "5"; // কঠোর সমান নয় (টাইপ চেক সহ)
let gt = 5 > 3; // বড়
let gte = 5 >= 5; // বড় বা সমান
let lt = 3 < 5; // ছোট
let lte = 3 <= 3; // ছোট বা সমান

// লজিকাল অপারেটর
let and = true && false; // AND
let or = true || false; // OR
let not = !true; // NOT

// টারনারি অপারেটর
let ternary = 5 > 3 ? "বড়" : "ছোট";
